std::vector<Rectangle> extract_components(const BinaryMatrix& image)
{
	int dr[4] = {0, 0, -1, 1};
	int dc[4] = {1, -1, 0, 0};
	auto [nrow, ncol] = image.dims();
	Matrix<int> seen{nrow, ncol, 0};
	
	std::vector<Rectangle> components{};

	for (int r = 0; r < nrow; r++) {
		for (int c = 0; c < ncol; c++) {
			if (seen.get_element(r, c) || image.get_element(r, c))
				continue;
			int min_r = std::numeric_limits<int>::max();
			int min_c = std::numeric_limits<int>::max();
			int max_r = std::numeric_limits<int>::min();
	    	int max_c = std::numeric_limits<int>::min();
			std::deque<std::pair<int, int>> queue{};
		
			auto handle = [&](int i, int j) {
				if (!image.in_bound(i, j) || image.get_element(i, j) || seen.get_element(i, j))
					return;
				queue.emplace_back(i, j);
				seen.set_element(i, j, 1);
				min_r = std::min(min_r, i);
				min_c = std::min(min_c, j);
				max_r = std::max(max_r, i);
				max_c = std::max(max_c, j);
			};

			handle(r, c);
			while (!queue.empty()) {
				auto [cr, cc] = queue.front();
				queue.pop_front();
				for (int d = 0; d < 4; d++)
					handle(cr + dr[d], cc + dc[d]);
			}
			components.emplace_back(min_r, min_c, max_r, max_c);
		}
	}
	return components;
}

Matrix<int> extract_image(const Matrix<int>& image, const Rectangle& box)
{
	// extract the image and resize it
	int min_r = box.min_r;
	int min_c = box.min_c;
	int max_r = box.max_r;
	int max_c = box.max_c;

	Matrix<int> subimage{max_r - min_r + 1, max_c - min_c + 1};

	for (int r = min_r; r <= max_r; r++) {
		for (int c = min_c; c <= max_c; c++) {
			subimage.set_element(r - min_r, c - min_c, image.get_element(r, c));
		}
	}
	return subimage;
}

std::string ocr(const Image& image) {
	Matrix<unsigned> matrix_form = Matrix{image.height, image.width, image.pixels};
	Matrix<int> bin_image = use_middle_val(matrix_form);

	print_binary(bin_image);

	std::vector<Rectangle> components = extract_components(bin_image);
	std::sort(components.begin(), components.end(), [](const Rectangle& r0, const Rectangle& r1) {
		return r0.min_c < r1.min_c;
	});

	std::vector<Matrix<int>> digit_images{};
	for (const Rectangle& box : components) {
		Matrix<int> subimage = extract_image(bin_image, box);
		digit_images.push_back(std::move(subimage));
	}

	std::ostringstream os;
	for (const Matrix<int>& digit_image : digit_images) {
		print_binary(digit_image);
		os << match(digit_image);
	}
	return os.str();
}


